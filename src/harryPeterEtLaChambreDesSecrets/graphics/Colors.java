package harryPeterEtLaChambreDesSecrets.graphics;

import java.awt.Color;

/**
 * The Class Colors. Contains all GUI colors used in this game.
 */
public abstract class Colors {

	/** The color of a possible direction. */
	public static final Color GOOD_EXIT = Color.white;

	/** The color of an impossible direction. */
	public static final Color BAD_EXIT = Color.darkGray;

	/** The color of the current room of the player. */
	public static final Color CURRENT_ROOM = Color.green;

	/** The color of the current room of Voldemort. */
	public static final Color VOLDEMORT_ROOM = Color.red;

	/** The color of the room name. */
	public static final Color ROOM_NAME_FOREGROUND = Color.white;

	/** The background color of a room. */
	public static final Color ROOM_BACKGROUND = new Color(153, 204, 255);

}
